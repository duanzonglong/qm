package com.cwms.qm.model.sitem;

public class ExtendProps
{
    private Integer isNew;

    private Integer boxMinPackageRate;

    private Integer boxLength;

    private Integer boxWidth;

    private Integer boxHeight;

    private Integer boxVolume;

    public Integer getIsNew()
    {
        return isNew;
    }

    public void setIsNew(Integer isNew)
    {
        this.isNew = isNew;
    }

    public Integer getBoxMinPackageRate()
    {
        return boxMinPackageRate;
    }

    public void setBoxMinPackageRate(Integer boxMinPackageRate)
    {
        this.boxMinPackageRate = boxMinPackageRate;
    }

    public Integer getBoxLength()
    {
        return boxLength;
    }

    public void setBoxLength(Integer boxLength)
    {
        this.boxLength = boxLength;
    }

    public Integer getBoxWidth()
    {
        return boxWidth;
    }

    public void setBoxWidth(Integer boxWidth)
    {
        this.boxWidth = boxWidth;
    }

    public Integer getBoxHeight()
    {
        return boxHeight;
    }

    public void setBoxHeight(Integer boxHeight)
    {
        this.boxHeight = boxHeight;
    }

    public Integer getBoxVolume()
    {
        return boxVolume;
    }

    public void setBoxVolume(Integer boxVolume)
    {
        this.boxVolume = boxVolume;
    }
}
